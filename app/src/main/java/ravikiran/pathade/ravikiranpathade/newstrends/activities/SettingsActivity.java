    /**
     * This fragment shows notification preferences only. It is used when the
     * activity is showing a two-pane settings UI.
     */
    @TargetApi(Build.VERSION_CODES.HONEYCOMB)
    public static class NotificationPreferenceFragment extends PreferenceFragment {
        private static final String JOB_TAG = "notifications_job";
        private FirebaseJobDispatcher dispatcher;

        @Override
        public void onCreate(Bundle savedInstanceState) {
            super.onCreate(savedInstanceState);
            addPreferencesFromResource(R.xml.pref_notification);
            setHasOptionsMenu(true);


            // Bind the summaries of EditText/List/Dialog/Ringtone preferences
            // to their values. When their values change, their summaries are
            // updated to reflect the new value, per the Android Design
            // guidelines.
            bindPreferenceSummaryToValue(findPreference(getResources().getString(R.string.notifications_new_message_ringtone)));

            Preference switchPref = findPreference(getResources().getString(R.string.notifications_new_message));
            switchPref.setOnPreferenceChangeListener(new Preference.OnPreferenceChangeListener() {
                @Override
                public boolean onPreferenceChange(Preference preference, Object o) {
                    boolean on = (boolean) o;
                    dispatcher = new FirebaseJobDispatcher(new GooglePlayDriver(getActivity()));
                    if (on) {
                        Job job = dispatcher.newJobBuilder().
                                setService(JobDispatcherForNotifications.class)
                                .setLifetime(Lifetime.FOREVER)
                                .setRecurring(true)
                                .setTag(JOB_TAG)
                                .setTrigger(Trigger.executionWindow(0,43200 )) //TODO Set for 12 hours - 43200
                                .setRetryStrategy(RetryStrategy.DEFAULT_EXPONENTIAL)
                                .setReplaceCurrent(false).
                                        setConstraints(Constraint.ON_ANY_NETWORK)
                                .build();

                        dispatcher.mustSchedule(job);

                    } else {
                        dispatcher.cancel(JOB_TAG);
                    }


                    return true;
                }
            });
        }

        @Override
        public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
            View view = super.onCreateView(inflater, container, savedInstanceState);
            if (view != null) {
                ListView preferencesList = (ListView) view.findViewById(android.R.id.list);
                preferencesList.setPadding(10, 100, 10, 10);
            }
            return view;
        }

        @Override
        public boolean onOptionsItemSelected(MenuItem item) {
            int id = item.getItemId();
            if (id == android.R.id.home) {
                startActivity(new Intent(getActivity(), SettingsActivity.class));
                return true;
            }
            return super.onOptionsItemSelected(item);
        }
    }

